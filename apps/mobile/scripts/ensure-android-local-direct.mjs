/**
 * Pre-build patch script for the Tauri Android project.
 *
 * Tauri re-generates the Android shell on `tauri android init`, which can
 * overwrite local patches. This script is run before every build/dev command
 * to ensure the following patches are always in place:
 *
 *  1. Enable cleartext HTTP — the app needs to connect to the local bridge
 *     daemon over plain HTTP during development and LAN pairing.
 *  2. Soft-input resize — tells Android to resize the WebView when the
 *     on-screen keyboard appears, preventing input fields from being hidden.
 *  3. Mixed content mode — allows the WebView (loaded from https://tauri.localhost
 *     in release builds) to fetch plain-HTTP bridge endpoints on the LAN.
 *  4. Native keyboard inset events — injects a listener in MainActivity that
 *     dispatches a `tauri-keyboard-inset-change` CustomEvent into the WebView
 *     so the front-end can react to keyboard height changes (e.g. adjust
 *     chat input position).
 *
 * Each patch is idempotent — safe to run multiple times.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { existsSync } from 'node:fs';
import { resolve } from 'node:path';

// --- Android project file paths (generated by `tauri android init`) ---

const gradleFile = resolve(
  process.cwd(),
  'src-tauri/gen/android/app/build.gradle.kts',
);
const manifestFile = resolve(
  process.cwd(),
  'src-tauri/gen/android/app/src/main/AndroidManifest.xml',
);
const mainActivityFile = resolve(
  process.cwd(),
  'src-tauri/gen/android/app/src/main/java/com/mycodexapp/mobile/MainActivity.kt',
);

/** Read a file, apply a transform, write it back only if the content changed. */
function rewriteFileIfPresent(file, transform) {
  if (!existsSync(file)) {
    return;
  }

  const original = readFileSync(file, 'utf8');
  const next = transform(original);
  if (next !== original) {
    writeFileSync(file, next);
  }
}

// --- Patch 1: Enable cleartext (HTTP) traffic for local bridge connectivity ---

rewriteFileIfPresent(gradleFile, (original) => {
  let seenPlaceholder = false;
  return original
    .split('\n')
    .flatMap((line) => {
      if (!line.includes('manifestPlaceholders["usesCleartextTraffic"]')) {
        return [line];
      }

      if (seenPlaceholder) {
        return [];
      }

      seenPlaceholder = true;
      return [line.replace(/"false"|"true"/, '"true"')];
    })
    .join('\n');
});

// --- Patch 2: Set windowSoftInputMode so the WebView resizes with the keyboard ---

rewriteFileIfPresent(manifestFile, (original) => {
  if (original.includes('android:windowSoftInputMode="adjustResize"')) {
    return original.replace(
      /android:windowSoftInputMode="[^"]+"/,
      'android:windowSoftInputMode="adjustResize"',
    );
  }

  return original.replace(
    'android:exported="true">',
    'android:exported="true"\n            android:windowSoftInputMode="adjustResize">',
  );
});

// --- Patch 3: Allow mixed content (HTTPS→HTTP) so release builds can reach the local bridge ---

rewriteFileIfPresent(mainActivityFile, (original) => {
  if (original.includes('mixedContentMode')) {
    return original;
  }

  return original.replace(
    'super.onWebViewCreate(webView)\n',
    [
      'super.onWebViewCreate(webView)',
      '',
      '    // Allow the WebView (loaded from https://tauri.localhost in release builds)',
      '    // to fetch plain-HTTP bridge endpoints on the LAN.',
      '    webView.settings.mixedContentMode = android.webkit.WebSettings.MIXED_CONTENT_ALWAYS_ALLOW',
      '',
    ].join('\n'),
  );
});

// --- Patch 4: Enable WebView debugging in debug builds only (connect via chrome://inspect) ---

rewriteFileIfPresent(mainActivityFile, (original) => {
  // Already patched (new conditional version)
  if (
    original.includes('BuildConfig.DEBUG') &&
    original.includes('setWebContentsDebuggingEnabled')
  ) {
    return original;
  }

  // Remove old unconditional version if present (comment + call together)
  let next = original.replace(
    /(?:^\s*\/\/ Enable WebView debugging[^\n]*\n)?^\s*android\.webkit\.WebView\.setWebContentsDebuggingEnabled\(true\)\s*\n/gm,
    '',
  );

  // Add import if missing (BuildConfig is auto-generated by Android Gradle plugin)
  if (!next.includes('import com.mycodexapp.mobile.BuildConfig')) {
    next = next.replace(
      'import android.os.Bundle\n',
      'import android.os.Bundle\nimport com.mycodexapp.mobile.BuildConfig\n',
    );
  }

  // Inject conditional debugging after mixed content mode
  if (!next.includes('setWebContentsDebuggingEnabled')) {
    next = next.replace(
      'webView.settings.mixedContentMode = android.webkit.WebSettings.MIXED_CONTENT_ALWAYS_ALLOW\n',
      [
        'webView.settings.mixedContentMode = android.webkit.WebSettings.MIXED_CONTENT_ALWAYS_ALLOW',
        '',
        '    // Enable WebView debugging in debug builds only (connect via chrome://inspect)',
        '    if (BuildConfig.DEBUG) {',
        '      android.webkit.WebView.setWebContentsDebuggingEnabled(true)',
        '    }',
        '',
      ].join('\n'),
    );
  }

  return next;
});

// --- Patch 5: Inject native keyboard-inset listener that dispatches JS events ---

rewriteFileIfPresent(mainActivityFile, (original) => {
  let next = original
    .replace('import android.util.Log\n', '')
    .replace(/^\s*Log\.i\("MyCodexAppKeyboard".*\n/gm, '')
    .replace(
      /^\s*window\.__MY_CODEX_APP_NATIVE_KEYBOARD_INSET__ = .*;\n/gm,
      '',
    );

  if (!next.includes('import android.webkit.WebView')) {
    next = next.replace(
      'import android.os.Bundle\n',
      'import android.os.Bundle\nimport android.webkit.WebView\n',
    );
  }

  if (!next.includes('import androidx.core.view.ViewCompat')) {
    next = next.replace(
      'import androidx.core.view.WindowCompat\n',
      'import androidx.core.view.ViewCompat\nimport androidx.core.view.WindowCompat\n',
    );
  }

  if (!next.includes('private var lastKeyboardInset = -1')) {
    next = next.replace(
      'class MainActivity : TauriActivity() {\n',
      'class MainActivity : TauriActivity() {\n  private var lastKeyboardInset = -1\n\n',
    );
  }

  if (!next.includes('window.dispatchEvent(')) {
    next = next.replace(
      '\n}\n',
      `

  override fun onWebViewCreate(webView: WebView) {
    super.onWebViewCreate(webView)

    ViewCompat.setOnApplyWindowInsetsListener(webView) { _, windowInsets ->
      val imeInsets = windowInsets.getInsets(WindowInsetsCompat.Type.ime())
      val systemBarInsets = windowInsets.getInsets(WindowInsetsCompat.Type.systemBars())
      val keyboardInset =
        if (windowInsets.isVisible(WindowInsetsCompat.Type.ime())) {
          maxOf(0, imeInsets.bottom - systemBarInsets.bottom)
        } else {
          0
        }

      if (keyboardInset != lastKeyboardInset) {
        lastKeyboardInset = keyboardInset
        webView.post {
          webView.evaluateJavascript(
            """
              window.dispatchEvent(
                new CustomEvent('tauri-keyboard-inset-change', {
                  detail: { height: $keyboardInset }
                })
              );
            """.trimIndent(),
            null,
          )
        }
      }

      windowInsets
    }
    ViewCompat.requestApplyInsets(webView)
  }
}
`,
    );
  }

  return next;
});
